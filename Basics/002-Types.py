# Consultar tipos de datos
# Tipo 'string'
print(type("Hello Python"))

# Tipo 'Integer'
print(type(5))

# Tipo 'Float'
print(type(1.5))

# Tipo 'Complex'
print(type(1 + 3j))

# Tipo 'Boolean'
print(type(True))
print(type(False))  # Tipo 'Boolean

# Tipo 'Lista'
print(type([1, 2, 3]))

# Tipo 'Dictionary'
personal_info = {"name": "Juan", "sur_name": "Parra", "alias": "JotaMario", "age": 29}
print(type(personal_info))
